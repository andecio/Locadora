    public void limparLista(JTable tabela) {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        if (model.getRowCount() > 0) {
            int tamanho = model.getRowCount();
            for (int i = 0; i <= tamanho - 1; i++) {
                model.removeRow(0);
            }
        }
    }

